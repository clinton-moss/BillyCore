package za.co.presencepeaksolutions.billy.core.rest.controller.greeting;

import java.util.concurrent.atomic.AtomicLong;

import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RestController;
import za.co.presencepeaksolutions.billy.core.greeting.Greeting;

@RestController
public class GreetingController {

	private static final String template = "Hello, %s!";
	private final AtomicLong counter = new AtomicLong();

	@GetMapping("/greeting")
	public Greeting greeting(@RequestParam(value = "name", defaultValue = "World") String name) {
		return new Greeting(counter.incrementAndGet(), String.format(template, name));
	}
        
        
	@GetMapping("/alive")
	public boolean alive(@RequestParam(value = "name", defaultValue = "World") String name) {
		return true;
	}
}